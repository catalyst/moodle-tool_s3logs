use tool_s3logs\local\client\s3_client;

/**
 * Get status checks for tool_s3logs.
 *
 * @return array
 */
function tool_s3logs_status_checks(): array {
    $client = new s3_client();

    if ($client->is_configured()) {
        return [
            new tool_s3logs\check\status()
        ];
    }

    return [];
}
